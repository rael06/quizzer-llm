import assert from "assert";
import { z } from "zod";
import PromptService from "../services/PromptService";

type ParsedAiMessage = {
  modelMessage: string;
  feedback: string;
  isCorrect: boolean;
};

export default class Answer {
  private _modelMessage: string;
  private _answer: string;
  private _feedback: string;
  private _isCorrect: boolean;

  private constructor(
    modelMessage: string,
    answer: string,
    feedback: string,
    isCorrect: boolean,
  ) {
    this._modelMessage = modelMessage;
    this._answer = answer;
    this._feedback = feedback;
    this._isCorrect = isCorrect;
  }

  public static build(userAnswer: string, message: string): Answer {
    // Extract feedback, expectedAnswer and isCorrect from the raw feedback generated by the model
    const { feedback, isCorrect } = Answer.parseFeedbackFromAi(message);

    return new Answer(message, userAnswer, feedback, isCorrect);
  }

  private static parseFeedbackFromAi(message: string): ParsedAiMessage {
    const regex = new RegExp(
      `${PromptService.getInstance().startToken}(.+)<\\+>(.+)`,
      "m",
    );
    const match = message.match(regex);
    assert(match, "Invalid feedback format");

    const [_, feedback, isCorrectStr] = match;

    const validatedMatch = z
      .object({
        feedback: z.string(),
        isCorrectStr: z.enum(["CORRECT", "INCORRECT"]),
      })
      .parse({
        feedback,
        isCorrectStr,
      });

    const validatedFeedback = validatedMatch.feedback;
    const validatedIsCorrect = validatedMatch.isCorrectStr === "CORRECT";

    return {
      modelMessage: message,
      feedback: validatedFeedback,
      isCorrect: validatedIsCorrect,
    };
  }

  public toView() {
    return {
      answer: this._answer,
      feedback: this._feedback,
      isCorrect: this._isCorrect,
    };
  }

  public get modelMessage(): string {
    return this._modelMessage;
  }

  public get answer(): string {
    return this._answer;
  }

  public get feedback(): string {
    return this._feedback;
  }

  public get isCorrect(): boolean {
    return this._isCorrect;
  }
}
